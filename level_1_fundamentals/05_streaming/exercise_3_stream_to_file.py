def demo_batch_reports():
    """Generate multiple clinical reports to separate files"""
    print("\n" + "=" * 70)
    print("DEMO 3: BATCH CLINICAL REPORTS")
    print("=" * 70)

    reports = [
        {
            "name": "Progress Note",
            "file": "output_progress_note.txt",
            "messages": [
                {"role": "system", "content": "You are a physician writing a SOAP progress note. Be concise but thorough. Educational purposes only."},
                {"role": "user", "content": "Write a progress note for day 3 of admission: 72M admitted for pneumonia, on levofloxacin, improving. Cough decreasing, O2 now 95% on RA (was 89% on admission). WBC trending down from 15.2 to 11.8. Appetite returning."}
            ],
            "max_tokens": 600,
        },
        {
            "name": "Consultation Note",
            "file": "output_consult_note.txt",
            "messages": [
                {"role": "system", "content": "You are a cardiologist writing a consultation note. Be professional and thorough. Educational purposes only."},
                {"role": "user", "content": "Cardiology consult for 65F with new onset atrial fibrillation found on telemetry after hip replacement surgery. Rate 120s, asymptomatic. PMH: HTN, DM2. CHA2DS2-VASc would be calculated."}
            ],
            "max_tokens": 800,
        },
        {
            "name": "Referral Letter",
            "file": "output_referral.txt",
            "messages": [
                {"role": "system", "content": "You are a PCP writing a referral letter to a specialist. Professional and concise. Educational purposes only."},
                {"role": "user", "content": "Refer 45M to GI for persistent GERD despite 8 weeks of omeprazole 40mg BID, now with dysphagia to solids. Needs EGD. PMH: obesity, smoking."}
            ],
            "max_tokens": 500,
        },
    ]

    print(f"\n📄 Generating {len(reports)} clinical reports...\n")

    for i, report in enumerate(reports, 1):
        print(f"\n{'═' * 70}")
        print(f"📝 Report {i}/{len(reports)}: {report['name']} → {report['file']}")
        print(f"{'═' * 70}\n")

        content, stats = stream_to_file(report["messages"], report["file"], report["max_tokens"])

        print(f"\n   ✅ Saved ({stats['file_size_bytes']} bytes, {stats['total_time_s']}s)")

    print(f"\n{'═' * 70}")
    print("📁 ALL FILES GENERATED:")
    for report in reports:
        if os.path.exists(report["file"]):
            size = os.path.getsize(report["file"])
            print(f"   📄 {report['file']} ({size} bytes)")


