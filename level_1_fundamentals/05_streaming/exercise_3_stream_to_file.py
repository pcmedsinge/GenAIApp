import os
import sys
import time
from datetime import datetime
from openai import OpenAI
from dotenv import load_dotenv

load_dotenv()
client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))


# ============================================================
# Stream to Display AND File Simultaneously
# ============================================================

def stream_to_file(messages, output_path, max_tokens=1500):
    """
    Stream response to both terminal display and a file.
    Returns: (full_content, stats_dict)
    """
    start_time = time.time()
    first_token_time = None
    full_content = ""
    chunk_count = 0

    # Ensure output directory exists
    os.makedirs(os.path.dirname(output_path) if os.path.dirname(output_path) else ".", exist_ok=True)

    try:
        stream = client.chat.completions.create(
            model="gpt-4o-mini",
            messages=messages,
            max_tokens=max_tokens,
            stream=True
        )

        # Open file for writing while streaming
        with open(output_path, "w") as f:
            # Write header to file
            header = f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n{'=' * 60}\n\n"
            f.write(header)

            for chunk in stream:
                if chunk.choices[0].delta.content is not None:
                    token = chunk.choices[0].delta.content

                    if first_token_time is None:
                        first_token_time = time.time()

                    # Write to BOTH terminal and file
                    print(token, end="", flush=True)
                    f.write(token)
                    f.flush()  # Flush file buffer too — ensures real-time writing

                    full_content += token
                    chunk_count += 1

            # Write footer to file
            f.write(f"\n\n{'=' * 60}\n")
            f.write(f"Generated by AI Clinical Documentation System\n")
            f.write(f"Model: gpt-4o-mini | Tokens: {chunk_count} chunks\n")
            f.write(f"DISCLAIMER: For educational purposes only.\n")

        print()  # Newline after stream

    except Exception as e:
        print(f"\n❌ Error: {e}")
        return None, {}

    end_time = time.time()
    stats = {
        "first_token_ms": round((first_token_time - start_time) * 1000) if first_token_time else 0,
        "total_time_s": round(end_time - start_time, 2),
        "chunks": chunk_count,
        "characters": len(full_content),
        "file_path": output_path,
        "file_size_bytes": os.path.getsize(output_path),
    }

    return full_content, stats


