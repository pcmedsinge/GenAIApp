def main():
    """Run the consent and disclosure workflow demonstration."""
    print("=" * 70)
    print("  Exercise 3: Consent and Disclosure Workflow")
    print("  Disclaimer → Consent → Generate → Watermark → Attest")
    print("=" * 70)

    mgr = ConsentManager()

    # Process scenarios
