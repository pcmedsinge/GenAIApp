"""
Exercise 3: Consent and Disclosure Workflow
=============================================

Skills practiced:
- Implementing consent gates before AI content generation
- Building disclosure/disclaimer templates
- Logging consent acknowledgments with timestamps
- Adding watermarks to AI-generated clinical content
- Creating physician attestation workflows

Healthcare context:
When AI generates clinical content—whether it's a draft progress note, a clinical
decision support suggestion, or a patient education handout—there must be clear
disclosure that the content is AI-generated. Users must acknowledge this before
the content is used. Key requirements:

- Disclaimer: Users see a clear statement that content is AI-generated
- Acknowledgment: Users actively confirm they understand the AI's limitations
- Consent logging: The system records who consented, when, and for what
- Watermarking: AI-generated content is clearly marked throughout
- Attestation: For clinical documents, a physician must attest to accuracy

Usage:
    python exercise_3_consent_workflow.py
"""

