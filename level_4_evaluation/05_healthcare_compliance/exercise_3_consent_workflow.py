import time
from datetime import datetime
from openai import OpenAI
from dotenv import load_dotenv

load_dotenv()
client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))


# ---------------------------------------------------------------------------
# Disclaimer templates
# ---------------------------------------------------------------------------

DISCLAIMERS = {
    "clinical_decision_support": (
        "⚠️ AI-GENERATED CLINICAL DECISION SUPPORT\n"
        "This content was generated by an AI system and is intended to assist, "
        "not replace, clinical judgment. The AI may produce inaccurate or incomplete "
        "information. All clinical decisions must be made by a qualified healthcare "
        "provider based on their assessment of the individual patient.\n"
        "Model: {model} | Generated: {timestamp}"
    ),
    "patient_education": (
        "ℹ️ AI-GENERATED PATIENT EDUCATION MATERIAL\n"
        "This educational content was created with AI assistance. While reviewed "
        "for general accuracy, it may not reflect your specific medical situation. "
        "Always follow the advice of your personal healthcare provider.\n"
        "Generated: {timestamp}"
    ),
    "clinical_documentation": (
        "📋 AI-ASSISTED CLINICAL DOCUMENTATION\n"
        "This document was drafted by an AI system and MUST be reviewed, edited, "
        "and attested by the responsible clinician before becoming part of the "
        "medical record. The AI draft may contain errors.\n"
        "Model: {model} | Generated: {timestamp} | Status: DRAFT — REQUIRES ATTESTATION"
    ),
}

WATERMARK_HEADER = "═══ AI-GENERATED CONTENT — NOT REVIEWED ═══"
WATERMARK_FOOTER = "═══ END AI-GENERATED CONTENT — REQUIRES CLINICAL REVIEW ═══"
ATTESTED_HEADER = "═══ AI-ASSISTED CONTENT — CLINICIAN REVIEWED AND ATTESTED ═══"


# ---------------------------------------------------------------------------
# Consent manager
# ---------------------------------------------------------------------------

class ConsentManager:
    """Manages consent and disclosure for AI-generated content."""

    def __init__(self):
        self.consent_log = []
        self.content_registry = []

    def display_disclaimer(self, content_type: str, model: str = "gpt-4o-mini") -> str:
        """Get the appropriate disclaimer for the content type."""
        template = DISCLAIMERS.get(content_type, DISCLAIMERS["clinical_decision_support"])
        return template.format(model=model, timestamp=datetime.now().strftime("%Y-%m-%d %H:%M"))

    def record_consent(self, user_id: str, content_type: str,
                       acknowledged: bool) -> dict:
        """Record a user's consent acknowledgment."""
        entry = {
            "consent_id": f"CONSENT-{int(time.time() * 1000)}",
            "timestamp": datetime.now().isoformat(),
            "user_id": user_id,
            "content_type": content_type,
            "acknowledged": acknowledged,
            "disclaimer_version": "1.0",
        }
        self.consent_log.append(entry)
        return entry

    def check_consent(self, user_id: str, content_type: str) -> bool:
        """Check if a user has given consent for a content type (within session)."""
        for entry in reversed(self.consent_log):
            if (entry["user_id"] == user_id and
                    entry["content_type"] == content_type and
                    entry["acknowledged"]):
                return True
        return False

    def watermark_content(self, content: str, content_type: str,
                          attested: bool = False) -> str:
        """Add watermark to AI-generated content."""
        header = ATTESTED_HEADER if attested else WATERMARK_HEADER
        footer = "" if attested else WATERMARK_FOOTER
        disclaimer = self.display_disclaimer(content_type)

        watermarked = f"{header}\n\n{disclaimer}\n\n{content}"
        if footer:
            watermarked += f"\n\n{footer}"
        return watermarked

    def register_content(self, content: str, content_type: str,
                         user_id: str, model: str) -> dict:
        """Register generated content in the content registry."""
        entry = {
            "content_id": f"CONTENT-{int(time.time() * 1000)}",
            "timestamp": datetime.now().isoformat(),
            "content_type": content_type,
            "requested_by": user_id,
            "model": model,
            "content_hash": hashlib.sha256(content.encode()).hexdigest()[:16],
            "content_length": len(content),
            "status": "generated",
            "attestation": None,
        }
        self.content_registry.append(entry)
        return entry

    def attest_content(self, content_id: str, attester_id: str,
                       accuracy_confirmed: bool, modifications: str = "") -> dict:
        """Record physician attestation for AI-generated content."""
        for entry in self.content_registry:
            if entry["content_id"] == content_id:
                entry["status"] = "attested" if accuracy_confirmed else "rejected"
                entry["attestation"] = {
                    "attester_id": attester_id,
                    "timestamp": datetime.now().isoformat(),
                    "accuracy_confirmed": accuracy_confirmed,
                    "modifications_made": modifications,
                }
                return entry
        return None

    def get_consent_summary(self) -> dict:
        """Get summary of consent activity."""
        if not self.consent_log:
            return {"total": 0}

        acknowledged = sum(1 for c in self.consent_log if c["acknowledged"])
        return {
            "total_consent_requests": len(self.consent_log),
            "acknowledged": acknowledged,
            "declined": len(self.consent_log) - acknowledged,
            "unique_users": len(set(c["user_id"] for c in self.consent_log)),
            "by_content_type": self._count_by_field(self.consent_log, "content_type"),
        }

    def get_content_summary(self) -> dict:
        """Get summary of generated content status."""
        if not self.content_registry:
            return {"total": 0}
        return {
            "total_content": len(self.content_registry),
            "by_status": self._count_by_field(self.content_registry, "status"),
            "by_type": self._count_by_field(self.content_registry, "content_type"),
        }

    @staticmethod
    def _count_by_field(entries: list, field: str) -> dict:
        counts = {}
        for e in entries:
            val = e.get(field, "unknown")
            counts[val] = counts.get(val, 0) + 1
        return counts


# ---------------------------------------------------------------------------
# AI content generation with consent workflow
# ---------------------------------------------------------------------------

def generate_with_consent(consent_mgr: ConsentManager, user_id: str,
                          content_type: str, prompt: str) -> dict:
    """
    Full consent workflow:
    1. Display disclaimer
    2. Record consent
    3. Generate content
    4. Watermark content
    5. Register content
    """
    result = {"status": "pending"}

    # Step 1: Display disclaimer
    disclaimer = consent_mgr.display_disclaimer(content_type)
    print(f"\n  {'─' * 55}")
    print(f"  {disclaimer}")
    print(f"  {'─' * 55}")

    # Step 2: Simulate consent (in production, this would be interactive)
    # For demo, we auto-acknowledge
    acknowledged = True
    consent_entry = consent_mgr.record_consent(user_id, content_type, acknowledged)
    print(f"\n  ✅ Consent recorded: {consent_entry['consent_id']}")

    if not acknowledged:
        result["status"] = "consent_declined"
        return result

    # Step 3: Generate content
    print(f"  Generating {content_type} content...")
    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[
            {
                "role": "system",
                "content": f"Generate {content_type.replace('_', ' ')} content. "
                           f"Be accurate and professional. Keep it concise."
            },
            {"role": "user", "content": prompt}
        ],
        temperature=0.3,
        max_tokens=400
    )
    raw_content = response.choices[0].message.content

    # Step 4: Watermark
    watermarked = consent_mgr.watermark_content(raw_content, content_type)

    # Step 5: Register
    reg_entry = consent_mgr.register_content(raw_content, content_type, user_id, "gpt-4o-mini")

    result.update({
        "status": "generated",
        "content_id": reg_entry["content_id"],
        "raw_content": raw_content,
        "watermarked_content": watermarked,
        "consent_id": consent_entry["consent_id"],
    })
    return result


